    @Test("Parse valid Base16 YAML file")
    func parseValidYAML() throws {
        let tmpDir = FileManager.default.temporaryDirectory
            .appendingPathComponent("shikki-test-\(UUID().uuidString)")
        try FileManager.default.createDirectory(at: tmpDir, withIntermediateDirectories: true)
        defer { try? FileManager.default.removeItem(at: tmpDir) }

        let content = """
        scheme: "Test Theme"
        base00: "111111"
        base01: "222222"
        base02: "333333"
        base03: "444444"
        base04: "555555"
        base05: "666666"
        base06: "777777"
        base07: "888888"
        base08: "999999"
        base09: "aaaaaa"
        base0A: "bbbbbb"
        base0B: "cccccc"
        base0C: "dddddd"
        base0D: "eeeeee"
        base0E: "ff0000"
        base0F: "00ff00"
        """
        let path = tmpDir.appendingPathComponent("test.yaml").path
        try content.write(toFile: path, atomically: true, encoding: .utf8)

