// MARK: - InlineBase16Color Tests

@Suite("InlineBase16Color")
struct InlineBase16ColorTests {

    @Test("Parse valid hex string")
    func parseValidHex() {
        let color = InlineBase16Color(hex: "bd93f9")
        #expect(color != nil)
        #expect(color?.r == 189)
        #expect(color?.g == 147)
        #expect(color?.b == 249)
    }

    @Test("Parse hex with hash prefix")
    func parseHexWithHash() {
        let color = InlineBase16Color(hex: "#ff5555")
        #expect(color != nil)
        #expect(color?.r == 255)
        #expect(color?.g == 85)
        #expect(color?.b == 85)
    }

    @Test("Reject invalid hex string")
    func rejectInvalidHex() {
        #expect(InlineBase16Color(hex: "zzzzzz") == nil)
        #expect(InlineBase16Color(hex: "abc") == nil)
        #expect(InlineBase16Color(hex: "") == nil)
    }

    @Test("Hex output matches input")
    func hexRoundTrip() {
        let color = InlineBase16Color(hex: "282a36")!
        #expect(color.hex == "282a36")
    }

    @Test("ansiBg produces valid ANSI background escape")
    func ansiBgEscape() {
        let color = InlineBase16Color(r: 40, g: 42, b: 54)
        #expect(color.ansiBg == "\u{1B}[48;2;40;42;54m")
    }
}

